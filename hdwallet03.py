def sha256(data):
	digest = hashlib.new("sha256")
	digest.update(data)
	return digest.digest()

def getWif(privkey):
	h=hex(privkey)[2:]
	while len(h)<128:
		h='0'+h
	pvk = b'\x80' + bytes.fromhex(h)
	wif = base58.b58encode_check(pvk + sha256(sha256(pvk))[:4])
	return wif

def int_to_bytes3(value, length = None):
	if not length and value == 0:
		result = [0]
	else:
		result = []
		for i in range(0, length or 1+int(math.log(value, 2**8))):
			result.append(value >> (i * 8) & 0xff)
		result.reverse()
	return bytearray(result)

def pvk_to_wif2(key_hex):
	return base58.b58encode_check(b'\x80' + sha256(key_hex))

